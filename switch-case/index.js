let score = parseInt(prompt("Inserisci il tuo voto"));

switch (score) {
    case 1: 
        console.log("voto ottimo");
        break;
    case 2:
        console.log("voto buono");
        break;
    case 3:
        console.log("voto sufficiente");
        break;
    case 4:
        console.log("voto insufficiente");
        break;
}